//swap number using third variable
package com.java;

public class Three {

	public static void main(String[] args) {
		int a=10;
		int b=5;
		System.out.println("Before swaping a & b values are:"+a+" "+b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("After swaping a & b values are:"+a+" "+b);
		

	}

}
